package ee.taltech.game.server.packets;

public class PacketPowerUpTaken {
    private float x; // Box2D coordinate of power-up
    private float y;
    private int gameId;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public int getGameId() {
        return gameId;
    }

    public void setGameId(int gameId) {
        this.gameId = gameId;
    }
}
